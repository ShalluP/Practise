package ProgramsJava;

public class ArithmeticOperators {

	public static void main(String[] args) {
		
		int a=60 ,b=30;
		
		int sum = a+b;
		System.out.println(sum);
		
		int sub = a-b;
		System.out.println(sub);
		
		int mulp = a*b;
		System.out.println(mulp);
		
		int div = a / b;
		System.out.println(div);
		
		
		
	}

}
